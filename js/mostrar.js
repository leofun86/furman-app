
  function Mostrar1 () {
    document.getElementById("caja1").style.display = "block";
  }
  function Ocultar1 () {
    document.getElementById("caja1").style.display = "none";
  }
  function Mostrar_Ocultar1 () {
    var caja = document.getElementById("caja1");

    if (caja.style.display == "none") {
      Mostrar1();
    }
    else {
      Ocultar1();
    }
  }

